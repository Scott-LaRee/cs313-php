  <div id="similar">
   <h1>Similarities to Cinderella</h1>
   <p>On its face Ella Enchanted is a reimagining of the Cinderella story.
     Her mother dies when she is young and her father remaries a woman who
	 has two daughters of her own. This woman and her daughters are terrible
	 to Ella and force her to be a servant in her own home. Ella falls in love
	 with Prince Charmont and in the end they get married. Sounds like Cinderella,
	 right?</p>
  </div>
  <div id="different">
   <h1>Major Differences from Cinderella</h1>
   <p>While it is clearly a Cinderella story, it is also very different. Firstly
     because of the curse. The curse creates a whole new twist to the Cinderella
	 story. Another major difference is that Ella's father does not die. Sir Peter
	 is simply a terrible father who doesn't care about anyone but himself. He is
	 a con man masquerading as a traveling merchant who is away from home most of 
	 the book. Another major difference is Ella's relationship with Prince Char.
	 They don't meet at the ball. (In the book there are actually three balls.) 
	 They have known each other for years and are good friends. They spend a great 
	 deal of time exchanging letters as Char travels to the neighboring kingdom.</p>
  </div>
  <div id="better">
   <h1>Why is it better than Cinderella?</h1>
   <p>The original Cinderella story has always bothered me. It's fine right up until
     the ball. Then she dances with the prince and falls in love with him after
	 knowing him 10 minutes. Then the next day he shows up at her house and asks her
	 to marry him, and she agrees! I know its a children's story, but that is just
	 ridiculous. You can not get to know someone well enough to marry them in so 
	 short a time!
   </p>
   <p>I love how Ella grows as a person throughout the book. She goes through life
     being forced to obey the whims of others until she finds something important 
	 enough that she wants to protect. Only then is she able to overcome the curse
	 and take charge of her own life. 
    </p>
    <p>I know that many people are tired of new
	 versions of the Cinderella story, but I feel that Gail Carson Levine's take on
	 this classic story makes it new all over again and it is well worth the effort
	 to read!
    </p>
