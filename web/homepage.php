   <h1>Main plot</h1>
   <p>Ella Enchanted by Gail Carson Levine is about a girl named Ella
     who is cursed at birth to be obedient. This means that any time
	 she is given a direct command, such as "Touch your nose," she has
	 no choice but to obey. She ocassionally tries to disobey but she 
	 becomes ill and eventually she gives in. The curse gets her into
	 a lot of trouble because very few people know about it and people 
	 give her commands on accident that she has to obey. At one point
	 she is told to "Run along and bump into someone else," for example.</p>
